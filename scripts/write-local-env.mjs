/**
 * Writes .env for local dev (matches docker-compose.yml postgres/redis).
 * Run: node scripts/write-local-env.mjs
 */
import { generateKeyPairSync, randomBytes } from "node:crypto";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const { privateKey, publicKey } = generateKeyPairSync("rsa", {
  modulusLength: 2048,
  publicKeyEncoding: { type: "spki", format: "pem" },
  privateKeyEncoding: { type: "pkcs8", format: "pem" },
});

const esc = (pem) => JSON.stringify(pem);
const refresh = randomBytes(32).toString("hex"); // 64 chars
const encKey = randomBytes(32).toString("hex");
const encIv = randomBytes(12).toString("hex");

const lines = [
  "# Local development — generated by scripts/write-local-env.mjs",
  "# Matches docker-compose.yml (postgres + redis)",
  "",
  `DATABASE_URL=postgresql://sync:sync_secret@localhost:5432/sync_db`,
  `REDIS_URL=redis://127.0.0.1:6379`,
  "",
  `JWT_PRIVATE_KEY=${esc(privateKey)}`,
  `JWT_PUBLIC_KEY=${esc(publicKey)}`,
  "",
  `JWT_REFRESH_SECRET=${refresh}`,
  "",
  `ENCRYPTION_KEY=${encKey}`,
  `ENCRYPTION_IV=${encIv}`,
  "",
  `FRONTEND_URL=http://localhost:5173`,
  `PORT=4000`,
  "",
];

writeFileSync(join(root, ".env"), lines.join("\n"), "utf8");
console.log("Wrote .env (local dev). Start DB: docker compose up -d");
